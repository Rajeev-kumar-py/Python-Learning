# Day 2 - Python Operators

# 1. Arithmetic Operators
a, b = 10, 3
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Remainder:", a % b)
print("Power:", a ** b)

# 2. Comparison Operators
print("Is a == b?", a == b)
print("Is a != b?", a != b)
print("Is a > b?", a > b)
print("Is a < b?", a < b)

# 3. Logical Operators
x, y = True, False
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)

# 4. Assignment Operators
x = 5
print("Initial x:", x)
x += 3
print("x after += 3:", x)
x -= 2
print("x after -= 2:", x)